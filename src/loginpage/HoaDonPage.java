    private void btTongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTongActionPerformed
        // TODO add your handling code here:
        SinhVienDAO sv = new SinhVienDAO();
        int soLuong = sv.countSinhVienInPhongKTX(txtMaPhong.getText());
        try {
            int a = Integer.parseInt(txtTienPhong.getText()) * soLuong;
            int b = Integer.parseInt(txtSoDien.getText()) * Integer.parseInt(txtDonGiaDien.getText());
            int c = Integer.parseInt(txtSoNuoc.getText()) * Integer.parseInt(txtDonGiaNuoc.getText());
            int d = Integer.parseInt(txtTienWifi.getText());
            int e = Integer.parseInt(txtTienVS.getText());
            int tong = a + b + c + d + e;
            if (soLuong == 0) {
                JOptionPane.showMessageDialog(this, "không tìm thấy mã phòng phù hợp", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else {
                txtTong.setText(String.valueOf(tong));
            }
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Chỉ được phép nhập số! Vui lòng nhập đúng để tính tổng.");
            txtTienPhong.setText("");
            txtSoDien.setText("");
            txtDonGiaDien.setText("");
            txtSoNuoc.setText("");
            txtDonGiaNuoc.setText("");
            txtTienWifi.setText("");
            txtTienVS.setText("");

        }
    }//GEN-LAST:event_btTongActionPerformed

