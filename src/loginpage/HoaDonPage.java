    public void loadDataToTable() {
        try {
            HoaDonDAO dao = new HoaDonDAO();
            List<HoaDon> list = dao.docFile();
            tblModel.setRowCount(0);
            for (HoaDon hd : list) {
                tblModel.addRow(new Object[]{
                    hd.getMaHD(), hd.getMaPhong(), hd.getTienPhong(), hd.getSoDien(), hd.getDonGiaDien(), hd.getSoNuoc(), hd.getDoGiaNuoc(), hd.getTienWifi(), hd.getTienVS(), hd.getTong()
                });

            }
            // HoaDon hd = new HoaDon();
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "lỗi", JOptionPane.ERROR_MESSAGE);
            //MessageDialog.showErrorMessage(this, e.getMessage(), "Lỗi");

        }
    }

    public void chiChoNhapSo(JTextField textField) {
        textField.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent e) {
                String text = textField.getText();
                if (!text.matches("\\d*")) { // Chỉ cho phép số
                    JOptionPane.showMessageDialog(null, "Chỉ được phép nhập số!");
                    textField.setText(text.replaceAll("[^\\d]", "")); // Xóa các ký tự không phải số
                }
            }
        });
    }

