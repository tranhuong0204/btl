    private void tbTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTaiKhoanMouseClicked
        // TODO add your handling code here:
        try {
            int row = tbTaiKhoan.getSelectedRow();
            if (row >= 0) {
                String obj = (String) tbTaiKhoan.getValueAt(row, 0);
                TaiKhoanDAO dao = new TaiKhoanDAO();
                List<TaiKhoan > list = dao.docFile();
                TaiKhoan tk = dao.getTKbyMaTK(obj, list);
                if (tk != null) {
                    txtTenTK.setText(tk.getTenTaiKhoan());
                    txtMatKhau.setText(tk.getMatKhau());
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            //MessageDialog.showErrorMessage(this, e.getMessage(), "Lỗi");
            JOptionPane.showMessageDialog(this, e.getMessage(), "lỗi", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_tbTaiKhoanMouseClicked

