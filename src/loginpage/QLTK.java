    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtTenTK, sb, "Tên tài khoản không được để trống");
        DataValidator.validateEmpty(txtMatKhau, sb, "Mật khẩu không được để trống");

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            TaiKhoanDAO dao = new TaiKhoanDAO();
            List<TaiKhoan> list = new ArrayList<>();
            TaiKhoan tk = new TaiKhoan(txtTenTK.getText(), txtMatKhau.getText());
            list.add(tk);
            dao.luuFile(list, true);
            JOptionPane.showMessageDialog(this, "TK đã được lưu", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadDataToTable();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtTenTK, sb, "Tên tài khoản không được để trống");
        DataValidator.validateEmpty(txtMatKhau, sb, "Mật khẩu không được để trống");
        DataValidator.validateEmpty(txtTim, sb, "Hãy tìm theo tên tk để sửa");

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa tài khoản không", "Hỏi", JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            TaiKhoanDAO dao = new TaiKhoanDAO();
            List<TaiKhoan> list = dao.docFile();
            TaiKhoan newTK = new TaiKhoan();
            newTK.setTenTaiKhoan(txtTenTK.getText());
            newTK.setMatKhau(txtMatKhau.getText());

            if (dao.suaTaiKhoan(txtTim.getText(), list, newTK)) {
                JOptionPane.showMessageDialog(this, "TK đã được sửa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                
                JOptionPane.showMessageDialog(this, "TK chưa được sửa", "Cảnh báo", JOptionPane.INFORMATION_MESSAGE);
            }
            dao.luuFile(list, false);
            loadDataToTable();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btSuaActionPerformed

